for (let item of coursesArray){
    let divWrap = document.createElement('div')
    divWrap.classList.add('wrapper')
    let div = document.createElement('div')
    let hWrapper = document.createElement('div')
    hWrapper.classList.add ('hWrapper')
    let modules = document.createElement('ul')
    modules.classList.add ('listOfModules')

    let h3 = document.createElement('h3')
    h3.innerText = `${item.title}`
    let h4 = document.createElement('h4')
    h4.innerText = `Тривалість курсів ${item.monthDuration} - місяців . Тобто ${item.hourDuration} - годин`

    document.body.appendChild(divWrap)
    divWrap.appendChild(div)
    div.appendChild(hWrapper)
    hWrapper.appendChild(h3)
    hWrapper.appendChild(h4)
    div.appendChild(modules)

    for (let element of item.modules){
        let li = document.createElement('li')
        li.innerText = `${element}`
        modules.appendChild(li)
    }

}